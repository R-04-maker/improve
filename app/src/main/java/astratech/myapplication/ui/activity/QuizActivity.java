package astratech.myapplication.ui.activity;

import android.os.Bundle;

import androidx.appcompat.app.AppCompatActivity;
import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentManager;

import astratech.myapplication.R;
import astratech.myapplication.ui.fragment.PeminatanFragment;
import astratech.myapplication.ui.fragment.QuizFragment;

public class QuizActivity extends AppCompatActivity {
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.quiz);
        FragmentManager fm = getSupportFragmentManager();
        Fragment fragment = fm.findFragmentById(R.id.quiz);
        if (fragment == null) {
            fragment = new QuizFragment().newInstance();
            fm.beginTransaction().replace(R.id.quiz, fragment).commit();
        }
    }
}









































































































































































































































































































































































